public class CirclePosition {
    public float radius = 0f;//粒子的半径
    public float angle = 0f;//粒子的角度
    public float time = 0f;//例子的时间

    public CirclePosition(float radius, float angle, float time)
    {
        this.radius = radius;
        this.angle = angle;
        this.time = time;
    }
}
